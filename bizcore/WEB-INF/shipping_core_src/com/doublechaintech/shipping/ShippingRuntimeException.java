
package com.doublechaintech.shipping;


public class ShippingRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public ShippingRuntimeException() {
        super();
    }


    public ShippingRuntimeException(String message) {
        super(message);
    }


    public ShippingRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public ShippingRuntimeException(Throwable cause) {
        super(cause);
    }


   
}












